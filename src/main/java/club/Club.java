package club;

public class Club {
	private String clubRank;
	private String clubName;
	private String clubMatch;
	private String pts;
	private String win;
	private String draw;
	private String lose;
	private String gf;
	private String ga;
	private String gd;
	private String recent;
	private String leaguetype;

	public String getClubRank() {
		return clubRank;
	}

	public void setClubRank(String clubRank) {
		this.clubRank = clubRank;
	}

	public String getClubName() {
		return clubName;
	}

	public void setClubName(String clubName) {
		this.clubName = clubName;
	}

	public String getClubMatch() {
		return clubMatch;
	}

	public void setClubMatch(String clubMatch) {
		this.clubMatch = clubMatch;
	}

	public String getPts() {
		return pts;
	}

	public void setPts(String pts) {
		this.pts = pts;
	}

	public String getWin() {
		return win;
	}

	public void setWin(String win) {
		this.win = win;
	}

	public String getDraw() {
		return draw;
	}

	public void setDraw(String draw) {
		this.draw = draw;
	}

	public String getLose() {
		return lose;
	}

	public void setLose(String lose) {
		this.lose = lose;
	}

	public String getGf() {
		return gf;
	}

	public void setGf(String gf) {
		this.gf = gf;
	}

	public String getGa() {
		return ga;
	}

	public void setGa(String ga) {
		this.ga = ga;
	}

	public String getGd() {
		return gd;
	}

	public void setGd(String gd) {
		this.gd = gd;
	}

	public String getRecent() {
		return recent;
	}

	public void setRecent(String recent) {
		this.recent = recent;
	}

	public String getLeaguetype() {
		return leaguetype;
	}

	public void setLeaguetype(String leaguetype) {
		this.leaguetype = leaguetype;
	}

}
